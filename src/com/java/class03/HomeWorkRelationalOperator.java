package com.java.class03;

public class HomeWorkRelationalOperator {
    public static void main(String[] args) {
        int a = 1;
        int b = -1;

        System.out.println(a > b);//t
        System.out.println(a < b);//f
        System.out.println(a >= b);//t
        System.out.println(a <= b);//f
        System.out.println(a != b);//t
        System.out.println(a == b);//f
    }
}
